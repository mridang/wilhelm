package main

import (
	"cmp"
	"errors"
	"fmt"
	"go/types"
	"os"
	"slices"
	"strings"

	"github.com/dave/jennifer/jen"
	"golang.org/x/tools/go/packages"
)

const (
	metav1TypeMetaName   = "TypeMeta"
	metav1ObjectMetaName = "ObjectMeta"
	apimachineryMetav1   = "k8s.io/apimachinery/pkg/apis/meta/v1"
	runtimePkg           = "k8s.io/apimachinery/pkg/runtime"
	schemaPkg            = "k8s.io/apimachinery/pkg/runtime/schema"
)

var (
	errGroupRequired     = errors.New("-group is required in crd mode")
	errAssertPkgRequired = errors.New("-assert-pkg is required in crd mode")
)

// crdConfig holds the inputs needed for one CRD-mode invocation.
type crdConfig struct {
	pkgName        string
	group          string
	packages       []string
	assertPkg      string
	clusterScoped  stringSet
	pluralOverride map[string]string
}

// kind describes one top-level Kind to be generated.
type kind struct {
	TypeName    string // e.g. "Application"
	TypePkgPath string // import path of the type
	Version     string // e.g. "v1alpha1" (last path segment)
	Plural      string // resource (lowercase plural)
	Namespaced  bool
}

// AssertName returns the matching assertion struct name in the CRD's
// assert subpackage.
func (k kind) AssertName() string { return k.TypeName + "Assertion" }

func runCRDMode(cfg crdConfig) (string, string, error) {
	if len(cfg.packages) == 0 {
		return "", "", errPackagesRequired
	}
	if cfg.group == "" {
		return "", "", errGroupRequired
	}
	if cfg.assertPkg == "" {
		return "", "", errAssertPkgRequired
	}
	kinds, err := discoverKinds(cfg)
	if err != nil {
		return "", "", fmt.Errorf("discovering kinds: %w", err)
	}
	slices.SortFunc(kinds, func(a, b kind) int {
		return cmp.Or(
			cmp.Compare(a.Version, b.Version),
			cmp.Compare(a.TypeName, b.TypeName),
		)
	})
	rendered, err := emitCRDFile(cfg, kinds)
	if err != nil {
		return "", "", fmt.Errorf("rendering: %w", err)
	}
	summary := fmt.Sprintf("crd mode (group=%s): %d Kind(s)", cfg.group, len(kinds))
	return rendered, summary, nil
}

// discoverKinds loads cfg.packages and returns every struct that looks
// like a top-level CRD Kind (embeds metav1.TypeMeta + metav1.ObjectMeta).
func discoverKinds(cfg crdConfig) ([]kind, error) {
	pkgsCfg := &packages.Config{
		Mode: packages.NeedTypes | packages.NeedName |
			packages.NeedSyntax | packages.NeedTypesInfo,
	}
	loaded, err := packages.Load(pkgsCfg, cfg.packages...)
	if err != nil {
		return nil, fmt.Errorf("packages.Load: %w", err)
	}
	var out []kind
	for _, pkg := range loaded {
		if pkg.Types == nil {
			continue
		}
		for _, e := range pkg.Errors {
			fmt.Fprintf(os.Stderr, "envgen: %s: package error: %s\n", pkg.PkgPath, e.Error())
		}
		out = append(out, kindsFromPackage(pkg, cfg)...)
	}
	return out, nil
}

// kindsFromPackage walks one package's scope for top-level Kinds.
func kindsFromPackage(pkg *packages.Package, cfg crdConfig) []kind {
	scope := pkg.Types.Scope()
	version := lastSegment(pkg.PkgPath)
	var out []kind
	for _, name := range scope.Names() {
		obj := scope.Lookup(name)
		if !obj.Exported() {
			continue
		}
		named, ok := obj.Type().(*types.Named)
		if !ok {
			continue
		}
		struc, ok := named.Underlying().(*types.Struct)
		if !ok {
			continue
		}
		if !isTopLevelKind(struc) {
			continue
		}
		out = append(out, kind{
			TypeName:    name,
			TypePkgPath: pkg.PkgPath,
			Version:     version,
			Plural:      pluralize(name, cfg.pluralOverride),
			Namespaced:  !cfg.clusterScoped.has(name),
		})
	}
	return out
}

// isTopLevelKind reports whether struc embeds both metav1.TypeMeta and
// metav1.ObjectMeta — the conventional shape of a CRD Kind.
func isTopLevelKind(struc *types.Struct) bool {
	var hasTypeMeta, hasObjectMeta bool
	for field := range struc.Fields() {
		if !field.Embedded() {
			continue
		}
		named, ok := field.Type().(*types.Named)
		if !ok {
			continue
		}
		obj := named.Obj()
		pkg := obj.Pkg()
		if pkg == nil || pkg.Path() != apimachineryMetav1 {
			continue
		}
		switch obj.Name() {
		case metav1TypeMetaName:
			hasTypeMeta = true
		case metav1ObjectMetaName:
			hasObjectMeta = true
		}
	}
	return hasTypeMeta && hasObjectMeta
}

// lastSegment returns the last "/"-separated segment of p.
func lastSegment(p string) string {
	idx := strings.LastIndex(p, "/")
	if idx < 0 {
		return p
	}
	return p[idx+1:]
}

// emitCRDFile renders the generated env subpackage source for the
// CRD-dynamic-client mode.
func emitCRDFile(cfg crdConfig, kinds []kind) (string, error) {
	f := jen.NewFile(cfg.pkgName)
	f.HeaderComment("Code generated by envgen. DO NOT EDIT.")
	for _, k := range kinds {
		emitCRDGetter(f, cfg, k)
		emitCRDGetterE(f, cfg, k)
		emitCRDAssert(f, cfg, k)
	}
	emitCRDAssertPartial(f, cfg, kinds)
	emitCRDAssertNone(f, cfg, kinds)
	var buf strings.Builder
	if err := f.Render(&buf); err != nil {
		return "", fmt.Errorf("render: %w", err)
	}
	return buf.String(), nil
}

// emitCRDGetter emits Get<Kind>(env, t, name) *pkg.Kind.
func emitCRDGetter(f *jen.File, _ crdConfig, k kind) {
	f.Commentf(
		"Get%s fetches a %s by name via the dynamic client, failing the test on error.",
		k.TypeName, k.TypeName,
	)
	f.Func().Id("Get"+k.TypeName).Params(
		jen.Id("env").Op("*").Qual(envPkgPath, "Env"),
		jen.Id("t").Op("*").Qual("testing", "T"),
		jen.Id("name").String(),
	).Op("*").Qual(k.TypePkgPath, k.TypeName).Block(
		jen.Id("t").Dot("Helper").Call(),
		jen.List(jen.Id("obj"), jen.Id("err")).Op(":=").Id("Get"+k.TypeName+"E").Call(
			jen.Id("env"), jen.Id("t"), jen.Id("name"),
		),
		jen.Qual(requirePkg, "NoError").Call(
			jen.Id("t"), jen.Id("err"),
			jen.Lit(fmt.Sprintf("failed to get %s %%s", k.TypeName)), jen.Id("name"),
		),
		jen.Return(jen.Id("obj")),
	)
	f.Line()
}

// emitCRDGetterE emits Get<Kind>E(env, t, name) (*pkg.Kind, error).
func emitCRDGetterE(f *jen.File, cfg crdConfig, k kind) {
	gvr := jen.Qual(schemaPkg, "GroupVersionResource").Values(jen.Dict{
		jen.Id("Group"):    jen.Lit(cfg.group),
		jen.Id("Version"):  jen.Lit(k.Version),
		jen.Id("Resource"): jen.Lit(k.Plural),
	})
	resourceChain := jen.Id("env").Dot("DynamicClient").Dot("Resource").Call(jen.Id("gvr"))
	if k.Namespaced {
		resourceChain = resourceChain.Dot("Namespace").Call(jen.Id("env").Dot("Namespace"))
	}
	f.Commentf(
		"Get%sE fetches a %s by name via the dynamic client, returning the error for non-existence checks.",
		k.TypeName, k.TypeName,
	)
	f.Func().Id("Get"+k.TypeName+"E").Params(
		jen.Id("env").Op("*").Qual(envPkgPath, "Env"),
		jen.Id("t").Op("*").Qual("testing", "T"),
		jen.Id("name").String(),
	).Parens(jen.List(
		jen.Op("*").Qual(k.TypePkgPath, k.TypeName),
		jen.Error(),
	)).Block(
		jen.Id("t").Dot("Helper").Call(),
		jen.Id("gvr").Op(":=").Add(gvr),
		jen.List(jen.Id("u"), jen.Id("err")).Op(":=").Add(resourceChain).
			Dot("Get").Call(
			jen.Id("env").Dot("Ctx"), jen.Id("name"),
			jen.Qual(metav1Pkg, "GetOptions").Values(),
		),
		jen.If(jen.Id("err").Op("!=").Nil()).Block(
			jen.Return(jen.Nil(), jen.Id("err")),
		),
		jen.Var().Id("out").Qual(k.TypePkgPath, k.TypeName),
		jen.If(jen.Id("err").Op(":=").Qual(runtimePkg, "DefaultUnstructuredConverter").
			Dot("FromUnstructured").Call(
			jen.Id("u").Dot("Object"), jen.Op("&").Id("out"),
		).Op(";").Id("err").Op("!=").Nil()).Block(
			jen.Return(jen.Nil(),
				jen.Qual("fmt", "Errorf").Call(
					jen.Lit(fmt.Sprintf("convert %s from unstructured: %%w", k.TypeName)),
					jen.Id("err"),
				),
			),
		),
		jen.Return(jen.Op("&").Id("out"), jen.Nil()),
	)
	f.Line()
}

// emitCRDAssert emits Assert<Kind>(env, t, name, asn).
func emitCRDAssert(f *jen.File, cfg crdConfig, k kind) {
	f.Commentf("Assert%s fetches a %s and runs a partial assertion against it.", k.TypeName, k.TypeName)
	f.Func().Id("Assert"+k.TypeName).Params(
		jen.Id("env").Op("*").Qual(envPkgPath, "Env"),
		jen.Id("t").Op("*").Qual("testing", "T"),
		jen.Id("name").String(),
		jen.Id("asn").Qual(cfg.assertPkg, k.AssertName()),
	).Block(
		jen.Id("t").Dot("Helper").Call(),
		jen.Qual(wilhelmAssertPkg, "Partial").Call(
			jen.Id("t"),
			jen.Id("Get"+k.TypeName).Call(jen.Id("env"), jen.Id("t"), jen.Id("name")),
			jen.Id("asn"),
			jen.Id("name"),
		),
	)
	f.Line()
}

// emitCRDAssertPartial emits package-level AssertPartial(env, t, name, asn)
// that dispatches to Assert<Kind> based on the concrete assertion type.
// When there are no Kinds, the function still exists but always falls
// through to t.Fatalf so callers get a clear error.
func emitCRDAssertPartial(f *jen.File, cfg crdConfig, kinds []kind) {
	f.Comment("AssertPartial dispatches assertion to the matching Assert<Kind> based on")
	f.Comment("the concrete type of assertion.")
	body := []jen.Code{jen.Id("t").Dot("Helper").Call()}
	if len(kinds) == 0 {
		body = append(body, jen.Id("t").Dot("Fatalf").Call(
			jen.Lit("AssertPartial: no Kind registered for assertion type %T"),
			jen.Id("assertion"),
		))
	} else {
		cases := make([]jen.Code, 0, len(kinds)*2+1)
		for _, k := range kinds {
			cases = append(cases,
				jen.Case(jen.Qual(cfg.assertPkg, k.AssertName())).Block(
					jen.Id("Assert"+k.TypeName).Call(
						jen.Id("env"), jen.Id("t"), jen.Id("name"), jen.Id("a"),
					),
				),
				jen.Case(jen.Op("*").Qual(cfg.assertPkg, k.AssertName())).Block(
					jen.Id("Assert"+k.TypeName).Call(
						jen.Id("env"), jen.Id("t"), jen.Id("name"), jen.Op("*").Id("a"),
					),
				),
			)
		}
		cases = append(cases, jen.Default().Block(
			jen.Id("t").Dot("Fatalf").Call(
				jen.Lit("AssertPartial: no Kind registered for assertion type %T"),
				jen.Id("assertion"),
			),
		))
		body = append(body, jen.Switch(
			jen.Id("a").Op(":=").Id("assertion").Assert(jen.Type()),
		).Block(cases...))
	}
	f.Func().Id("AssertPartial").Params(
		jen.Id("env").Op("*").Qual(envPkgPath, "Env"),
		jen.Id("t").Op("*").Qual("testing", "T"),
		jen.Id("name").String(),
		jen.Id("assertion").Qual(wilhelmAssertPkg, "Assertable"),
	).Block(body...)
	f.Line()
}

// emitCRDAssertNone emits AssertNone(env, t, name, asn) that asserts the
// resource does not exist.
func emitCRDAssertNone(f *jen.File, cfg crdConfig, kinds []kind) {
	cases := make([]jen.Code, 0, len(kinds)+1)
	for _, k := range kinds {
		cases = append(cases,
			jen.Case(
				jen.Qual(cfg.assertPkg, k.AssertName()),
				jen.Op("*").Qual(cfg.assertPkg, k.AssertName()),
			).Block(
				jen.List(jen.Id("_"), jen.Id("err")).Op(":=").
					Id("Get"+k.TypeName+"E").Call(jen.Id("env"), jen.Id("t"), jen.Id("name")),
				jen.Qual(requirePkg, "True").Call(
					jen.Id("t"),
					jen.Qual(apierrorsPkg, "IsNotFound").Call(jen.Id("err")),
					jen.Lit(fmt.Sprintf("%s %%q should not exist (err: %%v)", k.TypeName)),
					jen.Id("name"),
					jen.Id("err"),
				),
			),
		)
	}
	cases = append(cases,
		jen.Default().Block(
			jen.Id("t").Dot("Fatalf").Call(
				jen.Lit("AssertNone: no Kind registered for assertion type %T"),
				jen.Id("assertion"),
			),
		),
	)
	f.Comment("AssertNone asserts that the Kind implied by assertion does not exist")
	f.Comment("(the dynamic-client GET returns NotFound).")
	f.Func().Id("AssertNone").Params(
		jen.Id("env").Op("*").Qual(envPkgPath, "Env"),
		jen.Id("t").Op("*").Qual("testing", "T"),
		jen.Id("name").String(),
		jen.Id("assertion").Qual(wilhelmAssertPkg, "Assertable"),
	).Block(
		jen.Id("t").Dot("Helper").Call(),
		jen.Switch(jen.Id("assertion").Assert(jen.Type())).Block(cases...),
	)
}

// envPkgPath is the import path of wilhelm's core env package — the CRD
// subpackages take an *env.Env from this package.
const envPkgPath = "github.com/mridang/wilhelm/env"
